func PlacesArray() [7]int {
	val := 26
	var pA [7]int

	pA[0] = val
	pA[1] = (val * pA[0]) + val
	pA[2] = val + val*(pA[1]+pA[0])
	pA[3] = val + val*(pA[2]+pA[1]+pA[0])
	pA[4] = val + val*(pA[3]+pA[2]+pA[1]+pA[0])
	pA[5] = val + val*(pA[4]+pA[3]+pA[2]+pA[1]+pA[0])
	pA[6] = val + val*(pA[5]+pA[4]+pA[3]+pA[2]+pA[1]+pA[0])

	return pA
}

